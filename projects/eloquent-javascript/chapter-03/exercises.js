function min(num1, num2) {

  return Math.min(num1, num2);
  //Returns the smallest number in the set

}

////////////////////////////////////////////////////////////////////////////////
// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function isEven(num) {

  num = Math.abs(num);
  if (num === 0) 
    return true;
  else if (num === 1)
    return false;
  else 
    return isEven(num - 2);

}

////////////////////////////////////////////////////////////////////////////////
// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countChars(string, char) {

  var counter = 0;

  for (var i = 0; i < string.length; i++) {
    if (string[i] === char) {
      counter++;
    }
  }
  return counter;

}

////////////////////////////////////////////////////////////////////////////////
// countBs /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countBs(string) {

  var counterB = 0;

  for (var i = 0; i < string.length; i++) {
    if (string[i] === "B") {
      counterB++;
    }
  }
  return counterB;

}

